var operation = prompt("Insira o simbolo de operação");

if (operation == "+") {
    alert('Adição');
} else if (operation == "-") {
    alert('Subtração');
} else if (operation == "*") {
    alert('Multiplicação');
} else if (operation == "/") {
    alert('Divisão');
} else {
    alert('Operação não definida');
}



